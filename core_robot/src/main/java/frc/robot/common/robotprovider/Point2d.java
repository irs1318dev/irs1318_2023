package frc.robot.common.robotprovider;

public class Point2d
{
    public double x;
    public double y;

    /**
     * A point in 2 dimensions.
     * @param x typically in inches
     * @param y typically in inches
     */
    public Point2d(double x, double y)
    {
        this.x = x;
        this.y = y;
    }
}
